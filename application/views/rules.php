            <h2 class="text-center" id="anchorgeneral">General Rules</h2><br><br>
            <h6 class="text-secondary">
                <ul class="list-group list-group-flush">
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; <span class="font-weight-bold">VDM:</span> Also called <span class="font-weight-bold">Vehicle Death Match</span> Using any vehicle as a weapon (e.g. running people over, causing explosions including while sling loading, ramming, et). 
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp;<span class="font-weight-bold">RDM:</span> Also called <span class="font-weight-bold">Random Death Match</span> is shooting at someone without engaging in any form of quality role play is considered RDM. (e.g. Giving enough time for them to comply with your order. Count downs are not considered quality role-play., please at least attempt to create an interesting role-play. story before considering shooting.) Punishment is a ban.
                    </li>
                    <li class="list-group-item border-0">
                        <span class="font-weight-bold font-italic">NOTE  : This rule doesn’t apply in a RED ZONE.</span>
                    </li>
                    <li class="list-group-item border-0"> 
                        <i class="far fa-hand-point-right"></i> &nbsp; Using any exploits such as duplication of items or money. Punishment is a permanent ban without appeal. If you discover an exploit that nobody else knows about, please reveal it to a staff member/admin privately in order to ensure that it does not be abused in the future. 
                    </li>
                    <li class="list-group-item border-0">
                        <span class="font-weight-bold font-italic">NOTE : Combat logging, if found voluntary, at any point during RP is considered exploiting.</span>
                    </li> 
                    <li class="list-group-item border-0"> 
                        <i class="far fa-hand-point-right"></i> &nbsp; Combat storing, such as storing a vehicle whilst you are being chased, impounding/scrapping your vehicle to gain an advantage or to otherwise prevent role-play. will result in a ban. 
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; Trolling is a bannable offence. Unnecessary mic spam, disrespectful comms, message spam, blocking areas with vehicles, “pranking” or otherwise disruptive behaviour is considered trolling. 
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; Threats to report  disputes / Fail RP  in Voice in-game e.g.: “Government se bataake ban karvadenge” et is not allowed. If someone has RDMed you, you can follow the proper channels and report the person. 
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; IRPS does not condone any sort of real-life to in-game trading or vice versa. 
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; The EMS are here to help people. It is a bannable offence to cause harm to a medic, take them hostage or rob them. when there are less than 4 medics on the server. You cannot take a medic’s vehicle at any point. 
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; Please keep all aviation above 300m. You are not allowed to fly above any  Green, Blue or Purple Zones. 	
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; The EMS are here to help people. It is a bannable offence to cause harm to a medic, take them hostage or rob them. when there are less than 4 medics on the server. You cannot take a medic’s vehicle at any point. 
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; No one is allowed to stay/visit at Hospital and Police Department for no reason If you want to contact admins you can approach them using proper channels. If someone is doing that first they are warned. After 2-3 warnings they will be kicked and multiple offences will get you banned.
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; Fear is a universal emotion. When confronted with a hostile situation, you must show proper signs of fear and act accordingly. You cannot RP as a “fearless” person. 
