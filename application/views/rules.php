            <h2 class="text-center" id="anchorroleplay">Role-play Rules</h2><br><br>
            <h6 class="text-secondary">
                <ul class="list-group list-group-flush">
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; You must remain in character at all times unless an Admin/staff member authorises you to speak out of character.
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; Out of Character Chat (OOC), interactions must be made over side chat.
                        <ul class="list-group list-group-flush">
                            <li class="list-group-item border-0"><i class="fas fa-hand-point-right"></i> &nbsp; Extended OOC interactions are forbidden.</li>
                            <li class="list-group-item border-0"><i class="fas fa-hand-point-right"></i> &nbsp; You cannot abuse OOC or for RP. Eg: asking location, asking for help when incapacitated etc.</li>
                        </ul>
                    </li>
                    <li class="list-group-item border-0"> 
                        <i class="far fa-hand-point-right"></i> &nbsp; You can use Twitter to broadcast your message across the server like it is a message board. 
                        <ul class="list-group list-group-flush">
                            <li class="list-group-item border-0"><i class="fas fa-hand-point-right"></i> &nbsp; Behaviour rules apply when posting on Twitter. You cannot be unreasonably abusive. </li>
                            <li class="list-group-item border-0"><i class="fas fa-hand-point-right"></i> &nbsp; You cannot spam the Twitter chat. </li>
                            <li class="list-group-item border-0"><i class="fas fa-hand-point-right"></i> &nbsp; You cannot use Twitter or for RP. Eg: asking location, asking for help when incapacitated etc. </li>
                        </ul>
                    </li>
                    <li class="list-group-item border-0"> 
                        <i class="far fa-hand-point-right"></i> &nbsp; You must hot-mic all communications that have been made over TeamSpeak or Discord when they are relevant to influencing the course of role-play. This means that if you are communicating to fellow team members via TeamSpeak or Discord and you are in audio proximity range of another player (they can hear you), you must hot mic any commands given so the rival player can overhear. This rule does not apply if both sides are engaged in a gunfight together. 
                        <ul class="list-group list-group-flush">
                            <li class="list-group-item border-0"><i class="fas fa-hand-point-right"></i> &nbsp; You cannot communicate to friends/allies outside of audible range if your communicator is taken, either using TeamSpeak/Discord or In game chat. </li>
                         </ul>
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; Continuing to role-play. subjects of a sensitive nature after being requested to stop by another player is punishable by a ban. Eg: Racist/sexist roleplays
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; Baiting for the sake of a gunfight is not role-play. 
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; When joining the server, you cannot involve yourself in any ongoing gunfights involving your group/faction for 15 minutes. 
                    </li>
                    <li class="list-group-item border-0">
                        <span class="font-weight-bold font-italic">NOTE : This doesn’t apply when your connection fails and you reconnect. </span>
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; Using out of game information to influence the course of role-play. is considered meta-gaming and punishable by a ban. In character forum posts are considered in-game information.
                    </li>
                    <li class="list-group-item border-0">
                        <i class="far fa-hand-point-right"></i> &nbsp; You are not allowed to identify or call players in-game via ID.
                    </li>
                    <li class="list-group-item border-0">
                        <span class="font-weight-bold font-italic">NOTE : This rule doesn’t apply for staff members or admins when using it for disciplinary purposes.</span>
                    </li>
                </ul>
